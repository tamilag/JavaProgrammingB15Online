package day21;

public class M_NumbersStairs {
    public static void main(String[] args) {

        for (int x = 1; x <=5 ; x++) {
            for (int y = 1; y <=4 ; y++) {
                System.out.print(y+" ");
        }
            System.out.println();
        }

        for (int i = 1; i <=5; i++) {
            for (int j = 1; j <=i ; j++) {
                System.out.print(j+" ");
            }
            System.out.println();
        }







































    }
}
