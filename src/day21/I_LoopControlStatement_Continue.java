package day21;

public class I_LoopControlStatement_Continue {
    public static void main(String[] args) {
        //continue
        //if line continue reaches, the loop will skip to next iteration
        //we can use break and continue for any type of loop
        for (int i = 1; i <= 10; i++) {
            if (i%2==1){
                continue;
            }
            System.out.println(i);
        }































    }
}
