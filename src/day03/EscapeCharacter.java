package day03;

public class EscapeCharacter {
    public static void main(String[] args) {
        System.out.println("Hello \\ world");
        System.out.println("I like the book \"Java\" by Akbar");
        System.out.println("The movie \'The Lord Of The Rings\' is awesome");
        System.out.println("Hello\tWorld");
        System.out.println("Hello \n B15");
        //what is the equivalent of println and print, using one of the above

        System.out.print("This is a regular print \n");
        System.out.print("Test");
        System.out.println("This is println");
    }
}
