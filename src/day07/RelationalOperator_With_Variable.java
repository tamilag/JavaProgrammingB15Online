package day07;

public class RelationalOperator_With_Variable {
    public static void main(String[] args) {
        int myNum=10;
        System.out.println(myNum>5);
        System.out.println(myNum>-19);
        System.out.println(myNum>=5);
        System.out.println(myNum>=8);
        //== is used to check for equality
        System.out.println(myNum==6);
        // != is used yo check inequality, 10 is not equal 10, it will give false
        System.out.println(myNum!=10);


    }
}
